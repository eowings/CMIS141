public class Groupex4 {

	  public static void main(String[] args)  {
		  //Final Var should be all CAPS!
		  final double PI = 3.141592653589793;
		  double radius = 5.5;
		  //Just like in C the equation requires the 4/3 to have a decimal added to make the problem work 4.0/3.0.
		  double volume = 4.0/3.0 * PI * radius * radius * radius;
		  
		  System.out.println("The volume of a sphere that has a radius of " + radius + " is " + volume);
	  }
	}

